namespace Radebatz\ACache\Tests;

use Psr\Log\AbstractLogger;

/**
 * Test logger.
 */
class TestLogger extends AbstractLogger
{
    public $lines = array();

    /**
     * Logs with an arbitrary level.
     *
     * @param mixed $level
     * @param string $message
     * @param array $context
     * @return null
     */
    public function log($level, $message, array $context = array())
    {
        $this->lines[] = $message;
    }
}
